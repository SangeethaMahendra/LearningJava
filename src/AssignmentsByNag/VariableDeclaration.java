public class VariableDeclaration {
    public static void main (String[] args){
        int firstVariableDeclaration;
        firstVariableDeclaration = 10;
        System.out.println("Declaring Variable " + firstVariableDeclaration);
        int firstVariableInitialization = 20;
        System.out.println("initializing Variable " + firstVariableInitialization);
        firstVariableInitialization = 30;
        System.out.println("Assigning Variable " + firstVariableInitialization);
    }
}
