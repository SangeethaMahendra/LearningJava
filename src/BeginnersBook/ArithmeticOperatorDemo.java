package BeginnersBook;

public class ArithmeticOperatorDemo {
    public static void main(String[] args) {
        int num1 = 100;
        int num2 = 10;

        System.out.println("num1 + num2: " + (num1 + num2) );
        System.out.println("num1 - num2: " + (num1 - num2) );
        System.out.println("num1 * num2: " + (num1 * num2) );
        System.out.println("num1 / num2: " + (num1 / num2) );
        System.out.println("num1 % num2: " + (num1 % num2) );
    }
}
// output num1 + num2: 110
//num1 - num2: 90
//num1 * num2: 1000
//num1 / num2: 10
//num1 % num2: 0